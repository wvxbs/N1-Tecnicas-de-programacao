def Spacer():
    print("\n" * 3)

def main():
    Spacer()
    print("Return é utilizdo para retornar um valor ao chamador da função e finalizar a execução da função")
    Spacer()
    print("Return atua de certa forma como o ponto final da função, depois de um return nenhum código poder ser executado")
    Spacer()

main()